def _needs_polite_suffix(s: str) -> bool:
    if not s:
        return False
    s = s.rstrip()
    # ‡∏ñ‡πâ‡∏≤‡∏à‡∏ö‡∏î‡πâ‡∏ß‡∏¢‡∏Ñ‡∏≥‡∏™‡∏∏‡∏†‡∏≤‡∏û‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß ‡πÑ‡∏°‡πà‡πÄ‡∏ï‡∏¥‡∏°
    for end in _POLITE_ENDINGS:
        if s.endswith(end):
            return False
    return True

def _append_polite(text: str, polite: str = _POLITE_DEFAULT) -> str:
    """
    ‡πÄ‡∏ï‡∏¥‡∏°‡∏Ñ‡∏≥‡∏•‡∏á‡∏ó‡πâ‡∏≤‡∏¢‡∏™‡∏∏‡∏†‡∏≤‡∏û‡πÉ‡∏´‡πâ‡∏ó‡∏∏‡∏Å‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡πÅ‡∏ö‡∏ö‡∏û‡∏≠‡∏î‡∏µ ‡πÜ
    """
    out: List[str] = []
    for line in text.split("\n"):
        line = line.rstrip()
        if not line:
            out.append(line)
            continue
        if _needs_polite_suffix(line):
            # ‡πÑ‡∏°‡πà‡∏™‡∏ô‡πÉ‡∏à‡∏ä‡∏ô‡∏¥‡∏î‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏´‡∏°‡∏≤‡∏¢‡∏ó‡πâ‡∏≤‡∏¢‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡∏°‡∏≤‡∏Å‡∏ô‡∏±‡∏Å ‡πÄ‡∏ï‡∏¥‡∏°‡πÅ‡∏ö‡∏ö‡∏™‡∏±‡πâ‡∏ô
            out.append(f"{line} {polite}")
        else:
            out.append(line)
    return "\n".join(out)

